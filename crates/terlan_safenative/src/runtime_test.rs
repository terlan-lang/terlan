/// Extracts a handle from a successful reply term.
///
/// Inputs:
/// - `reply`: runtime reply expected to contain a handle.
///
/// Output:
/// - `Some(handle)` when the reply is a handle success.
/// - `None` after asserting the reply had an unexpected shape.
///
/// Transformation:
/// - Pattern-matches the stable reply term without unwrap/expect.
fn handle_reply(reply: SafeNativeReplyTerm) -> Option<SafeNativeHandle> {
    let SafeNativeReplyTerm::Ok(SafeNativeTerm::Handle { id, generation }) = reply else {
        return None;
    };
    Some(SafeNativeHandle { id, generation })
}

/// Builds a handle term from a runtime handle.
///
/// Inputs:
/// - `handle`: opaque runtime handle.
///
/// Output:
/// - Stable term that carries the handle id and generation.
///
/// Transformation:
/// - Copies handle identity into the term shape expected by runtime calls.
fn handle_term(handle: SafeNativeHandle) -> SafeNativeTerm {
    SafeNativeTerm::Handle {
        id: handle.id,
        generation: handle.generation,
    }
}

/// Extracts a text value from a successful reply term.
///
/// Inputs:
/// - `reply`: runtime reply expected to contain text.
///
/// Output:
/// - `Some(text)` when the reply is a text success.
/// - `None` for any other reply shape.
///
/// Transformation:
/// - Pattern-matches the stable reply term without panicking.
fn text_reply(reply: SafeNativeReplyTerm) -> Option<String> {
    let SafeNativeReplyTerm::Ok(SafeNativeTerm::Text(value)) = reply else {
        return None;
    };
    Some(value)
}

/// Extracts an optional handle from a successful reply term.
///
/// Inputs:
/// - `reply`: runtime reply expected to contain an optional handle.
///
/// Output:
/// - `Some(handle)` when the reply is `OptionalHandle(Some(_))`.
/// - `None` for missing rows or unexpected reply shapes.
///
/// Transformation:
/// - Uses the same term shape generated by resource-backed optional results.
fn some_handle_reply(reply: SafeNativeReplyTerm) -> Option<SafeNativeHandle> {
    let SafeNativeReplyTerm::Ok(SafeNativeTerm::OptionalHandle(Some(handle))) = reply else {
        return None;
    };
    Some(handle)
}

/// Reads the optional live Postgres test URL.
///
/// Inputs:
/// - `test_name`: name used in the skip diagnostic.
///
/// Output:
/// - `Some(url)` when `TERLAN_TEST_POSTGRES_URL` is configured.
/// - `None` when the live handler test should be skipped.
///
/// Transformation:
/// - Reads only the process environment and emits a skip line for ordinary
///   unit test runs without a database.
fn live_postgres_url(test_name: &str) -> Option<String> {
    match std::env::var("TERLAN_TEST_POSTGRES_URL") {
        Ok(url) => Some(url),
        Err(_) => {
            eprintln!("skipping {test_name}; TERLAN_TEST_POSTGRES_URL is not set");
            None
        }
    }
}

/// Verifies JSON can execute through the term runtime path.
///
/// Inputs:
/// - JSON text, object key, and operation ids.
///
/// Output:
/// - Test passes when parse/get/as-string returns the expected text.
///
/// Transformation:
/// - Exercises term decoding, resource-backed dispatch, opaque handle
///   storage, and reply encoding together.
#[test]
fn runtime_executes_json_operations_through_terms() {
    let mut runtime = SafeNativeRuntime::new();
    let Some(root) = handle_reply(runtime.call(
        "std.data.json.parse",
        &[SafeNativeTerm::Text(String::from(r#"{"name":"Ada"}"#))],
    )) else {
        return;
    };
    let Some(name) = handle_reply(runtime.call(
        "std.data.json.get",
        &[
            SafeNativeTerm::Handle {
                id: root.id,
                generation: root.generation,
            },
            SafeNativeTerm::Text(String::from("name")),
        ],
    )) else {
        return;
    };

    assert_eq!(
        runtime.call(
            "std.data.json.as_string",
            &[SafeNativeTerm::Handle {
                id: name.id,
                generation: name.generation,
            }],
        ),
        SafeNativeReplyTerm::Ok(SafeNativeTerm::Text(String::from("Ada")))
    );
}

